    public synchronized void addChatMessage(RoomMessages.ChatMessage message) {
        if( message.info ) {
            appendToPane(ChatArea, " *** " + message.chatMessage + "\n", Color.MAGENTA);
        } else {
            appendToPane(ChatArea, message.playerId, Color.BLUE);
            appendToPane(ChatArea, ": " + message.chatMessage + "\n", Color.BLACK);
        }
    }
    
    private void appendToPane(JTextPane tp, String msg, Color c)
    {
        StyledDocument doc = tp.getStyledDocument();

        Style style = tp.addStyle("I'm a Style", null);
        StyleConstants.setForeground(style, c);

        try { doc.insertString(doc.getLength(), msg,style); }
        catch (BadLocationException e){}
        
        ChatArea.select(doc.getLength(), doc.getLength());
    }


