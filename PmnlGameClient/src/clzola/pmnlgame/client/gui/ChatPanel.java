    private void InputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InputActionPerformed
        if( Input.getText().trim().length() > 0 ) {
            RoomMessages.ChatMessage chatMessage = new RoomMessages.ChatMessage();
            chatMessage.info= false;
            chatMessage.chatMessage = Input.getText();
            view.client.sendTCP(chatMessage);
            this.Input.setText("");
        }
    }//GEN-LAST:event_InputActionPerformed

