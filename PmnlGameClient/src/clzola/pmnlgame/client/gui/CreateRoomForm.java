    private boolean validateRoomName(String roomName) {
        if( roomName.trim().length() == 0 ) {
            JOptionPane.showMessageDialog(this, "Please enter room's name", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
