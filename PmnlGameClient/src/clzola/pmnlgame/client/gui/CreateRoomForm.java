    private void CreateRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateRoomActionPerformed
        if( validateRoomName(this.RoomName.getText()) ) {
            try {
                String roomId = this.RoomName.getText().trim();
                String password = this.RoomPassword.getText();
                boolean visible = this.RoomVisibility.isSelected();
                
                if( password.length() > 0) {
                    MessageDigest md = MessageDigest.getInstance("MD5");
                    byte[] hash = md.digest(password.getBytes("UTF-8"));

                    //converting byte array to Hexadecimal String
                    StringBuilder sb = new StringBuilder(2*hash.length);
                    for(byte b : hash) sb.append(String.format("%02x", b&0xff));

                    password = sb.toString();
                } else password = null;
                
                ServerMessages.CreateJoinRoomMessage message = new ServerMessages.CreateJoinRoomMessage();
                message.roomId = roomId;
                message.password = password;
                message.isVisible = visible;
                message.playersLimit = 4;
                
                ((ClientWindow) getParent()).client.sendTCP(message);
                this.dispose();
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(CreateRoomForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(CreateRoomForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CreateRoomActionPerformed

